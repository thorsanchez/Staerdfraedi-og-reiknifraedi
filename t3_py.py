#1 raða lista
def sort(A):
    B = A.copy()
    i = 1
    while i < len(B):
        j = i
        while j > 0 and B[j-1] > B[j]:
            B[j], B[j-1] = B[j-1], B[j]
            j -= 1
        i += 1
    return B

svar = sort([3,8,1,2,5,4])
print(svar)