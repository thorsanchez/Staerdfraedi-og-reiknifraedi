"""3. Skrifið fall snúavið(L) sem snýr við lista."""

def snuavid(L):
    n = len(L)
    M = []
    for i in range(n-1, -1, -1):
        M.append(L[i])
    return M
svar= snuavid([1,2,3,4])
print(svar)

"""6  Meðaltal og staðalfrávik"""

import math

def statistics(x):
    n = len(x)
    m = sum(x) / n
    s = math.sqrt(sum((i - m)**2 for i in x) / (n - 1))
    return (m, s)
svar = statistics([3,7,7,1])
svar2 = statistics([1,3,2001])
print(svar)
print(svar2)

