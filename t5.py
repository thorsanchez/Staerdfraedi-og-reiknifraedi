import numpy as np
import matplotlib.pyplot as plt

(a,b) = (-5, 2)
x = np.linspace(a,b)
plt.figure(figsize=(9,6))
plt.grid(True)
plt.axvline(color='k')
plt.axhline(color='k')
plt.box(False)
plt.plot(x, x * np.sin(x))
plt.xlim([a,b])
plt.tick_params(length=0)
plt.show()

