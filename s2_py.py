def bolurodun(x):
    n = len(x)
    vixlad = True
    while vixlad:
        vixlad = False
        for i in range(1, n):
            if x[i-1] > x[i]:
                x[i-1], x[i] = x[i], x[i-1]
                vixlad = True
    return x

# testum forritið
