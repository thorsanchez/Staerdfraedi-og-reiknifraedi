from math import sin

def f(x):
    if x == 0:
        return 0
    else:
        return sin(x)/x

def Simpsons(f,a,b,n):
    if n % 2 != 0:
      return None

    h = (b-a)/n
    first = f(a)
    last = f(b)

    x = a
    sum = 0
    for i in range(n-1):
        x += h
        value = f(x)
        if i % 2 == 0:
            sum +=4 * value
        else:
            sum += 2 * value

    total = (h/3)*(first+sum+last)
    return round(total, 4)

print(Simpsons(f,1,2,2))

