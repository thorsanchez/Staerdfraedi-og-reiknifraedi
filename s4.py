from urllib.request import urlopen

def is_vowel(c):
    vowels = "aeiouAEIOU"
    return c in vowels

f = urlopen("https://cs.hi.is/python/ord.txt")
words = []

for line in f:
    ord = line.decode().strip()
    words.append(ord)

max_consonants = 0
result = []

for ord in words:
    vowels = sum(1 for c in ord if is_vowel(c))
    consonants = len(ord) - vowels
    if vowels == 1 and consonants > max_consonants:
        result = [ord]
        max_consonants = consonants

    elif vowels == 1 and consonants == max_consonants:
        result.append(ord)

print("Finnið þau orð í skránni sem hafa einn sérhljóða og hámarksfjölda samhljóða:")
print(" ".join(result))

