def shell_röðun(A):
    N = len(A)
    interval = N // 2
    while interval >= 1:
        for i in range(interval):
            for j in range(i, N, interval):
                k = j
                while k >= interval and A[k-interval] > A[k]:
                    A[k], A[k-interval] = A[k-interval], A[k]
                    k -= interval
        interval //= 2
    return A

