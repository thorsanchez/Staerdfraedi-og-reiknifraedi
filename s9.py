def print_with_nulls(lst, nulls=4):
    for i, num in enumerate(lst):
        num_str = "{:,.0f}".format(num)
        num_str = num_str.replace(",", "," + "0" * nulls)
        print(num_str, end="")
        if i < len(lst) - 1:
            print(",", end=" ")
    print()
