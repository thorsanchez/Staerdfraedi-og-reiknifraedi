def two_like_cards(hand):
    values = [card[1] for card in hand]
    for value in values:
        if values.count(value) == 2:
            return True
    return False

#2)
def three_like_cards(hand):
    values = [card[1] for card in hand]
    for value in values:
        if values.count(value) == 3:
            return True
    return False

#3)
def full_house(hand):
    values = [card[1] for card in hand]
    for value in values:
        if values.count(value) == 3:
            for other_value in values:
                if other_value != value and values.count(other_value) == 2:
                    return True
    return False

#4)
def color(hand):
    suits = [card[0] for card in hand]
    for suit in suits:
        if suits.count(suit) == 5:
            return True
    return False

